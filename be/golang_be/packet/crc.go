func crc16(buf []uint8) uint16 {
	var lenght uint16 = uint16(len(buf))
	var counter uint16
	var crc uint16 = 0xDEAD
	for counter = 0; counter < lenght; counter++ {
		crc = (crc << 8) ^ crc16tab[((crc>>8)^uint16(buf[counter]))&0x00FF]
	}
	return crc
}

