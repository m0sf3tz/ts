func crc32(buf []uint8) uint32 {
	var lenght = len(buf)
	var crc uint32 = 0xBABE
	for counter := 0; counter < lenght; counter++ {
		crc = crc32Table[(crc^uint32(buf[counter]))&0xff] ^ (crc >> 8)
	}

	return crc
}

